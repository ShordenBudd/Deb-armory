get_choice() {
    echo "Who is the target?"
    echo "I - Iptables"
    echo "T - Tcpdump"
    read choice
}
while true; do
    get_choice

    case "$choice" in
        
        I)  echo "Killing iptables..."
            iptables -F
            iptables -F -t mangle
            iptables -X
            iptables -P INPUT ACCEPT
            iptables -P OUTPUT ACCEPT
            iptables -P FORWARD ACCEPT
            rm -rf /iptables/after.iptables
            break
            ;;

        T)
            echo "Killing tcpdump..."
            rm -rf /tcpdump
            break
            ;;
        *)
            echo "Invalid choice."
            ;;
    esac
done