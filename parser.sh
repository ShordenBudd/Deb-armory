get_choice() {
    echo "Choose logs to parse"
    echo "P - Port scan log"
    echo "I - ICMP scan log"
    read choice
}

while true; do
    get_choice

    case "$choice" in
        
        I)  
            journalctl -k -r -g "PING"
            break
            ;;

        T)
            journalctl -k -r -g "Port scan"
            break
            ;;
        *)
            echo "Invalid choice."
            ;;
    esac
done