extension="pcap"

read -p "Input port to listen:" port

while true; do
    i=0
    while [ -e "${base_filename}${i:+$i}.$extension" ]; do
        i=$((i + 1))
    done

    new_filename="${base_filename}${i:+$i}.$extension"

    sudo tcpdump -i "$port" -s 20000 -w "$new_filename"
    echo "Capturing packets to $new_filename"

    read -p "Press Enter to stop capturing or Ctrl+C to exit: " input
    if [ -z "$input" ]; then
        break
    fi
done
